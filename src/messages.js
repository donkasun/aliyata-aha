export function getMessage(distance, hit) {
  const pool = hit ? MESSAGES.hit
    : distance <= 60  ? MESSAGES.near
    : distance <= 150 ? MESSAGES.medium
    : MESSAGES.far;
  return pool[Math.floor(Math.random() * pool.length)];
}
